fun main() {
    println("Hello, World!") // print line with new line
    print("Hello, ") // print without new line
    print("baby!") // continue printing on same line
    println() // print blank line

    println("namaste " + "bhai") // concatenate and print
    val age = 21
    println("Age: $age") // print variable with $
    println("2 + 3 = ${2 + 3}") // print expression result
}
